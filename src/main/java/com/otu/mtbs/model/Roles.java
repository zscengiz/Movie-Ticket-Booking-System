package com.otu.mtbs.model;

/**
 *
 * @author zcengiz
 */
public class Roles {
    
    private Integer id;
    private String role;

    public Roles() {
    }

    public Roles(Integer id, String role) {
        this.id = id;
        this.role = role;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
 
}
